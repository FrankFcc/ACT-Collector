        pw = jPasswordField2.getText();
        String username = jTextField1.getText();
        String pw = jPasswordField2.getText();
        String sjs = jTextField3.getText();

        if (username == null || username.equals("")) {
            JOptionPane.showMessageDialog(null, "Please insert your account.");
            return;
        }

        if (pw == null || pw.equals("")) {
            JOptionPane.showMessageDialog(null, "Please insert your password.");
            return;
        }

        if (sjs == null || sjs.equals("")) {
            JOptionPane.showMessageDialog(null, "Please insert your identification number.");
            return;
        }

        if (suijishu.equals(sjs)) {

            if (Data.identification(username, pw)) {
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        Main.username = username;
                        Main.login.setVisible(false);
                        Main.MainScreen = new MainScreen();
                        Main.MainScreen.setVisible(true);
                    }

                });
            } else {
                JOptionPane.showMessageDialog(null, "Account or password is incorrect.");
                return;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Wrong Identification code");
            return;
        }
